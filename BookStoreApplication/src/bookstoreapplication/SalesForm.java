    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String selected = (String) prodCombo.getSelectedItem();
        //String clients = (String) clientCombo.getSelectedItem();
        saveBtn.setEnabled(true);
        clientCombo.setEnabled(false);
        if (billField.getText().isEmpty()) {
            error1.setText("Заполните номер накладной");
        } else {
            saveBtn.setEnabled(true);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            String count = countField.getText();
            String sum = sumField.getText();
            String price = priceField.getText();
            Double sum_itog = Double.parseDouble(sum);
            int n = jTable1.getRowCount();
            if (n == 0) {
                model.addRow(new Object[]{selected, count, price, sum});
                itogField.setText(sum);

            } else if (proverka() == true) {
                error.setText("Продукт уже добавлен");
            } else {
                error.setText(" ");
                model.addRow(new Object[]{selected, count, jLabel6.getText(), sum});
                for (int i = 0; i < n; i++) {
                    String itog = (String) jTable1.getValueAt(i, 3);
                    sum_itog += Double.parseDouble(itog);
                }
                itogField.setText(String.valueOf(sum_itog));
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

