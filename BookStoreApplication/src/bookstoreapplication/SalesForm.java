    private void dateSet() {
        Calendar cal = Calendar.getInstance();
        jXDatePicker1.setDate(cal.getTime());
        SimpleDateFormat dateFormat = new SimpleDateFormat("HH:mm:ss");
        String d = dateFormat.format(new java.util.Date());
        timeField.setText(d);
    }




    private void ClearField() {
        dateSet();
        saveBtn.setEnabled(false);
        billField.setText("");
        itogField.setText("");
        countField.setText("");
        sumField.setText(" ");
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
    }

    private boolean checkBill(int id) {
        boolean res = false;
        try {
            ResultSet rs = connection.select("number_invoce", "order_store", "number_invoce = " + id);
            if (rs.next()) {
                res = true;

            } else {
                res = false;
            }
        } catch (Exception e) {
        }
        return res;
    }

