    private void countFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_countFieldKeyReleased
        try {
            Double count = Double.parseDouble(countField.getText());
            Double price = Double.parseDouble(priceField.getText());
            if (count < 1) {
                error.setText("Количество должно быть > 1");
                jButton2.setEnabled(false);
                sumField.setText("");
            } else {
                jButton2.setEnabled(true);
                Double sum = price * count;
                sumField.setText(String.valueOf(sum));
                error.setText(" ");
            }

        } catch (NumberFormatException e) {
            if (countField.getText().isEmpty()) {
                error.setText(" ");
            } else {
                error.setText("Неверный ввод. Введите числовое значение");
            }

        }

    }//GEN-LAST:event_countFieldKeyReleased


    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        Integer invoice = Integer.parseInt(billField.getText());
        if (checkBill(invoice) == true) {
            error1.setText("Номер накладной существует");
        } else {
            String clients = (String) clientCombo.getSelectedItem();
            DateFormat sysDate = new SimpleDateFormat("yyyy-MM-dd");
            String data = sysDate.format(jXDatePicker1.getDate()) + " " + timeField.getText();
            if (result.isEmpty()) {
                int n = jTable1.getRowCount();
                for (int i = 0; i < n; i++) {
                    int prod_id = product.get(jTable1.getValueAt(i, 0).toString());
                    String volume =  jTable1.getValueAt(i, 1).toString();
                    String summa = jTable1.getValueAt(i, 3).toString();
                    try {
                        connection.insert("order_store(id_book, date_order, count_books, summ_order, id_employee, number_invoce)", "'" + prod_id + "','" + data + "','" + volume + "','" + summa + "','" + client.get(clients) + "','" + billField.getText() + "'");
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Реализация не проведена");
                    }
                }
                JOptionPane.showMessageDialog(null, "Реализация проведена");
                stockTable();
                error.setText(" ");
                clientCombo.setEnabled(true);
                btnEnabled();
                ClearField();
            } 
            result.clear();
            fillBudget();
        }
    }//GEN-LAST:event_saveBtnActionPerformed

