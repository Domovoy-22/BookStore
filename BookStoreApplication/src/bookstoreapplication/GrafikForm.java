    private void jTableWageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableWageMouseClicked
        try {
            int row = jTableWage.getSelectedRow();
            String tableClick = (jTableWage.getModel().getValueAt(row, 0).toString());
            resultSet = databaseConnection.select("b.id_employee, b.name_employee , c.name_position ,c.salary ,b.wtime ,b.premia ", "employee as b, position as c", "b.id_position = c.id_position and b.id_employee = " + tableClick);
            if (resultSet.next()) {
                String add1 = resultSet.getString("id_employee");
                idLabel.setText(add1);
                idLabel.setVisible(false);
                String add2 = resultSet.getString("name_employee");
                surnameField.setText(add2);
                String add3 = resultSet.getString("wtime");
                wtimeField.setText(add3);
                String add4 = resultSet.getString("name_position");
                postField.setText(add4);
                String add5 = resultSet.getString("premia");
                premiaField.setText(add5);
                String add6 = resultSet.getString("salary");
                wageField.setText(add6);
            }
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_jTableWageMouseClicked

    private void searchFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchFieldKeyReleased
        TableRowSorter<TableModel> sorter = new TableRowSorter<>(jTableWage.getModel());
        jTableWage.setRowSorter(sorter);
        String text = searchField.getText();
        if (text.length() == 0) {
            sorter.setRowFilter(null);
        } else {
            try {
                sorter.setRowFilter(RowFilter.regexFilter(text));
            } catch (PatternSyntaxException e) {
                System.err.println("Bad regex pattern");
            }
        }
    }//GEN-LAST:event_searchFieldKeyReleased

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (error.getText().isEmpty()) {
            if (wtimeField.getText().isEmpty() || premiaField.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Заполните поля");
            } else {
                try {
                    String valueId = idLabel.getText();
                    String valueWtime = wtimeField.getText();
                    String valuePrem = premiaField.getText();
                    databaseConnection.update("employee", "wtime = '" + valueWtime + "',premia = '" + valuePrem + "'", "id_employee= " + valueId);
                    JOptionPane.showMessageDialog(null, "Запись обновлена");
                    idLabel.setText("");
                    wtimeField.setText("");
                    premiaField.setText("");
                } catch (SQLException e) {
                    System.out.println(e.toString());
                }
                Update_table();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

