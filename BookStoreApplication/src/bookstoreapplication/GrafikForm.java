    private void Update_table() {
        DefaultTableModel dtm = new DefaultTableModel() {
            @Override
            public Class getColumnClass(int column) {
                Class returnValue;
                if ((column >= 0) && (column < getColumnCount())) {
                    returnValue = getValueAt(0, column).getClass();
                } else {
                    returnValue = Object.class;
                }
                return returnValue;
            }
        };
        try {
            resultSet = databaseConnection.select(" b.id_employee,b.name_employee, c.name_position,c.salary ,b.wtime,b.premia", "employee as b,position as c", "b.id_position = c.id_position ");
            Object[] columnNames = {"id", "ФИО", "Должность", "Оклад", "Отработанное время", "Премия"};
            dtm.setColumnIdentifiers(columnNames);
            while (resultSet.next()) {
                int i = resultSet.getInt("id_employee");
                String d = resultSet.getString("name_employee");
                String f = resultSet.getString("name_position");
                Object a = resultSet.getObject("premia");
                double b = resultSet.getInt("salary");
                Object c = resultSet.getObject("wtime");
                dtm.addRow(new Object[]{i, d, f, b, c, a});
            }
            jTableWage.setModel(dtm);
            jTableWage.removeColumn(jTableWage.getColumnModel().getColumn(0));
            TableRowSorter sorter = new TableRowSorter(dtm);
            jTableWage.setRowSorter(sorter);
        } catch (Exception e) {
            System.out.print(e.toString());
        }
    }

