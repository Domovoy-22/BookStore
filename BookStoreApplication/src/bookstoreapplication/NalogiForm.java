    private void UpdateTable() {
        idField.setVisible(false);
        try {
            ResultSet rs = connection.select("*", "nalogi", null);
            if (rs.next()) {
                String add1 = rs.getString("idnalog");
                idField.setText(add1);
                idField.setVisible(false);
                String add2 = rs.getString("prof_souz");
                souzField.setText(add2);
                String add3 = rs.getString("prof_vznos");
                vznosField.setText(add3);
                String add4 = rs.getString("soc_fond");
                fondField.setText(add4);

            }
        } catch (Exception e) {
        }
    }
