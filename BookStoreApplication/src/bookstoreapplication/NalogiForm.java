    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (error.getText().isEmpty()) {
            if (souzField.getText().isEmpty() || vznosField.getText().isEmpty() || fondField.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Заполните поля");
            } else {
                try {
                    String valueId = idField.getText();
                    String valueSouz = souzField.getText();
                    String valueVznos = vznosField.getText();
                    String valueFond = fondField.getText();
                    System.out.println(valueId + " " + valueSouz);
                    connection.update("nalogi", "prof_souz = '" + valueSouz + "',prof_vznos = '" + valueVznos + "',soc_fond = '" + valueFond + "'", "idnalog = 1");
                    JOptionPane.showMessageDialog(null, "Запись обновлена");
                } catch (Exception e) {
                }
                UpdateTable();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void souzFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_souzFieldKeyReleased
        if (souzField.getText().isEmpty()) {
            error.setText("");
        } else {
            String s = souzField.getText();
            error.setText(valid.isInteger(s));
        }
    }//GEN-LAST:event_souzFieldKeyReleased

