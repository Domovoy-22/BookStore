    private void countFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_countFieldKeyReleased

        try {
            Double count = Double.parseDouble(countField.getText());
            Double price = Double.parseDouble(priceField.getText());
            if (count < 1) {
                error.setText("Количество должно быть > 1");
                sumField.setText("");
            } else {
                jButton2.setEnabled(true);
                Double sum = price * count;
                sumField.setText(String.valueOf(sum));
                error.setText(" ");
            }
        } catch (NumberFormatException e) {
            if (countField.getText().isEmpty()) {
                error.setText(" ");
            } else {
                error.setText("Неверный ввод. Введите числовое значение");
            }

        }
    }//GEN-LAST:event_countFieldKeyReleased

