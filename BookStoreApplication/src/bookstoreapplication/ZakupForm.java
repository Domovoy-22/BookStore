    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
      Integer invoice = Integer.parseInt(billField.getText());
        if (checkBill(invoice) == true) {
            error1.setText("Номер накладной существует");
        } else 
        {
        String supplier = (String) supplierCombo.getSelectedItem();
        DateFormat sysDate = new SimpleDateFormat("yyyy-MM-dd");
        String data = sysDate.format(jXDatePicker1.getDate()) + " " + timeField.getText();
        if (result.isEmpty()) {
            Double b = Double.parseDouble(budgetLabel.getText());
            Double itog = Double.parseDouble(itogField.getText());
            Double s = b - itog;
            try {
                int n = jTable1.getRowCount();
                for (int i = 0; i < n; i++) {
                    int rawId = raws.get(jTable1.getValueAt(i, 0).toString());
                    String volume = jTable1.getValueAt(i, 1).toString();
                    double price = Double.parseDouble( jTable1.getValueAt(i, 2).toString());
                    String summa = jTable1.getValueAt(i, 3).toString();
                    try {
                        connection.insert("stock(id_book,date_stock,id_provider,count_stock, price_for_one, summ_stock,number_invoice)", "'" + rawId + "','" + data + "','" + suppliers.get(supplier) + "','" + volume + "','" + price + "','" + summa + "','" + billField.getText() + "'");
                    } catch (SQLException e) {
                         JOptionPane.showMessageDialog(null, "Поставка не проведена");
                    }
                }
                JOptionPane.showMessageDialog(null, "Поставка проведена");
                error.setText(" ");
                stockTable();
                fillBudget();
                supplierCombo.setEnabled(true);
                btnEnabled();
                ClearField();
                
            } catch (Exception e) {
            }

        } 
        else {
             JOptionPane.showMessageDialog(null, "Недостаточно денег на расчетном счету");
        }

        result.clear();
        }
    }//GEN-LAST:event_saveBtnActionPerformed


    private void itog() {

        Double sum_itog = 0.0;
        int n = jTable1.getRowCount();
        for (int i = 0; i < n; i++) {
            String itog = (String) jTable1.getValueAt(i, 3);
            sum_itog += Double.parseDouble(itog);
        }
        itogField.setText(String.valueOf(sum_itog));
    }


    private void ClearField() {
        saveBtn.setEnabled(false);
        billField.setText("");
        itogField.setText("");
        countField.setText("");
        sumField.setText(" ");
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
    }

    private void fillBudget() {
       try {
            ResultSet rs = connection.customSelect("select Summ_budget from Budget where ID = 1");
            while (rs.next()) {
                Double budget = rs.getDouble("Summ_budget");
                budgetLabel.setText(budget.toString());
            }
        } catch (Exception e) {
        }
    }
    
    
    
