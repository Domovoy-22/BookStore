    private void billFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_billFieldKeyReleased
        try {
            int n = jTable1.getRowCount();
            if (!billField.getText().isEmpty()) {
                Integer invoice = Integer.parseInt(billField.getText());
                error1.setText(" ");
                if (checkBill(invoice) == true) {
                    error1.setText("Номер накладной существует");
                } else {
                    error1.setText(" ");
                }
            } else {
                error1.setText(" ");
            }
            if (n != 0) {
                saveBtn.setEnabled(true);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Введите номер накладной");
            countField.setText("");
        }
    }//GEN-LAST:event_billFieldKeyReleased
    private boolean checkBill(int id) {
        boolean res = false;
        try {
            ResultSet rs = connection.select("number_invoice", "stock", "number_invoice= " + id);
            if (rs.next()) {
                res = true;

            } else {
                res = false;
            }
        } catch (Exception e) {
        }
        return res;
    }

